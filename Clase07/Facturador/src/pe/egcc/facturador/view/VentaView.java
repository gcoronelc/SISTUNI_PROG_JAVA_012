  private void btnProcesarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcesarActionPerformed
    
    try {
      // Datos
      String tipo = cboTipo.getSelectedItem().toString();
      double total = Double.parseDouble(txtTotal.getText());
      // Proceso
      Item[] repo = control.procesar(tipo, total);
      // Cargar reporte en tabla
      DefaultTableModel tabla;
      tabla = (DefaultTableModel) tablaRepo.getModel();
      tabla.setRowCount(0); // Elimina todos los datos
      for (Item o : repo) {
        Object[] rowData = {o.getConcepto(), o.getValor()};
        tabla.addRow(rowData);
      }
    } catch (NullPointerException e) {
      JOptionPane.showMessageDialog(rootPane, 
              "Falta seleccionar el tipo de documento.", 
              "VENTA ERROR", JOptionPane.ERROR_MESSAGE);
    } catch (NumberFormatException e) {
      JOptionPane.showMessageDialog(rootPane, 
              "Falta el total o no es un n√∫mero.", 
              "VENTA ERROR", JOptionPane.ERROR_MESSAGE);
    }
    
  }//GEN-LAST:event_btnProcesarActionPerformed

